void ProcessIRFrame(void)
{
  IR_NEC_Frame_t frame;
  char msg[64];

  frame = IR_NEC_GetFrame();

  if (!frame.ready)
  {
    return;
  }

  /* Hiển thị byte command lên 2 LED 7 thanh dưới dạng HEX */
  app_last_command = frame.command;
  Set7SegHexByte(app_last_command);

  /* Gửi debug ra Hercules */
  snprintf(msg, sizeof(msg),
           "RAW=%08lX ADDR=%02X CMD=%02X\r\n",
           frame.raw32,
           frame.address,
           frame.command);
  Debug_Print(msg);

  /* Xử lý chức năng theo nút */
  if (frame.command == IR_CMD_OK)
  {
    HAL_GPIO_TogglePin(GPIOG, GPIO_PIN_14);   // LED4
  }
  else if (frame.command == IR_CMD_1)
  {
    LEDBar_SetMode(LEDBAR_MODE_RUN_L);
  }
  else if (frame.command == IR_CMD_2)
  {
    LEDBar_SetMode(LEDBAR_MODE_RUN_R);
  }
  else if (frame.command == IR_CMD_3)
  {
    LEDBar_SetMode(LEDBAR_MODE_FLASH);
  }
  else if (frame.command == IR_CMD_4)
  {
    LEDBar_SetMode(LEDBAR_MODE_SPOT_BUMPER);
  }

  IR_NEC_ClearFrame();
}

